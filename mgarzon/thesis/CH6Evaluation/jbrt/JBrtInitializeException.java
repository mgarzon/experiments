package org.jbrt.client.exception;

public class JBrtInitializeException extends JBrtException {

	private static final long serialVersionUID = 1L;

	public JBrtInitializeException() {
		super();
	}

	public JBrtInitializeException(String message, Throwable cause) {
		super(message, cause);
	}

	public JBrtInitializeException(String message) {
		super(message);
	}

	public JBrtInitializeException(Throwable cause) {
		super(cause);
	}

}
